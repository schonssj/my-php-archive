<?php

// An attribution operator is used to define a variable by a value atribution.

$var = 0;
echo $var . "<br>";
$var += 5; // Sum.
echo $var . "<br>";
$var -= 3; // Subtraction.
echo $var . "<br>";
$var *= 3; // Multiplication.
echo $var . "<br>";
$var /= 3; // Divison.
echo $var . "<br>";
++$var;    // Pre-increment.
echo $var . "<br>";
--$var;    // Pre-decrement.
echo $var . "<br>";
$var++;    // Post-increment.
echo $var . "<br>";
$var--;    // Post-decrement.
echo $var . "<br>";
